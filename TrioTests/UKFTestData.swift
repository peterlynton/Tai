    // Parse CSV data
    let lines = testData.components(separatedBy: .newlines).filter { !$0.isEmpty }
    let formatter = ISO8601DateFormatter()
    formatter.formatOptions = [.withInternetDateTime, .withFractionalSeconds]

    var readings: [BloodGlucose] = []
    for line in lines {
        let parts = line.components(separatedBy: ",")
        guard parts.count == 2,
              let date = formatter.date(from: parts[0]),
              let glucose = Int(parts[1])
        else {
            continue
        }

        let reading = BloodGlucose(
            sgv: glucose,
            direction: nil,
            date: Decimal(date.timeIntervalSince1970 * 1000),
            dateString: date,
            unfiltered: nil,
            filtered: nil,
            noise: nil,
            glucose: glucose,
            type: "sgv"
        )
        readings.append(reading)
    }

    print("=== UKF Smoothing Test ===")
    print("Total readings: \(readings.count)")
    print("")

    // Run UKF smoothing
    var filter = UnscentedKalmanFilter()
    let smoothed = filter.smooth(readings)

    // Output results in CSV format
    print("Timestamp,Raw,Smoothed,Difference")
    for (raw, smooth) in zip(readings, smoothed) {
        let rawValue = raw.glucose ?? 0
        let smoothValue = smooth.glucose ?? 0
        let diff = smoothValue - rawValue

        let dateFormatter = ISO8601DateFormatter()
        let dateStr = dateFormatter.string(from: raw.dateString)

        print("\(dateStr),\(rawValue),\(smoothValue),\(diff)")
    }

    // Calculate statistics
    let differences = zip(readings, smoothed).map { raw, smooth -> Int in
        (smooth.glucose ?? 0) - (raw.glucose ?? 0)
    }

    let avgDiff = Double(differences.reduce(0, +)) / Double(differences.count)
    let maxDiff = differences.max() ?? 0
    let minDiff = differences.min() ?? 0

    print("")
    print("=== Statistics ===")
    print("Average difference: \(String(format: "%.2f", avgDiff)) mg/dL")
    print("Max difference: \(maxDiff) mg/dL")
    print("Min difference: \(minDiff) mg/dL")

    // Check for wild oscillations
    var consecutiveFlips = 0
    var maxFlips = 0
    for i in 1 ..< differences.count {
        if (differences[i] > 0 && differences[i - 1] < 0) || (differences[i] < 0 && differences[i - 1] > 0) {
            consecutiveFlips += 1
            maxFlips = max(maxFlips, consecutiveFlips)
        } else {
            consecutiveFlips = 0
        }
    }

    print("Max consecutive sign flips in difference: \(maxFlips)")
    if maxFlips > 10 {
        print("⚠️ WARNING: Detected wild oscillations (sign flips > 10)")
    } else {
        print("✓ No wild oscillations detected")
    }
